    static double convertor(String x) 
    {
    double ans = 0;
        if (x.equals (  "..."))
        {
            ans = 0;
        }
        else if (x.equals  ("A"))
        {
            ans = 4.00 ;
        }
        else if (x.equals  ("A-"))
        {
            ans = 3.67 ;
        }
        else if (x.equals  ("B+"))
        {
            ans = 3.33 ;
        }
        else if (x.equals  ("B"))
        {
            ans = 3.00 ;
        }
        else if (x.equals  ("B-"))
        {
            ans = 2.67 ;
        }
        else if (x.equals  ("C+"))
        {
            ans = 2.33 ;
        }
        else if (x.equals  ("C"))
        {
            ans = 2.00 ;
        }
        else if (x.equals  ("C-"))
        {
            ans = 1.67 ;
        }
        else if (x.equals  ("D+"))
        {
            ans = 1.33 ;
        }
        else if (x.equals  ("D"))
        {
            ans = 1.00  ;
        }
        else if (x.equals  ("D-"))
        {
            ans = 0.67 ;
        }
        else if (x.equals  ("F"))
        {
            ans = 0.00 ;
        }
     return ans;
  }
    

