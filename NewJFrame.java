    private void calcButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcButtonActionPerformed
        // Calculate Gpa
        String course1_grade, course2_grade, course3_grade, course4_grade, course5_grade, course6_grade;
        String credit1, credit2, credit3, credit4, credit5, credit6;
        int credit1_value, credit2_value, credit3_value, credit4_value, credit5_value, credit6_value;
        double gpa;
        
        course1_grade = courseGrade1.getItemAt(courseGrade1.getSelectedIndex());
        course2_grade = courseGrade2.getItemAt(courseGrade2.getSelectedIndex());
        course3_grade = courseGrade3.getItemAt(courseGrade3.getSelectedIndex());
        course4_grade = courseGrade4.getItemAt(courseGrade4.getSelectedIndex());
        course5_grade = courseGrade5.getItemAt(courseGrade5.getSelectedIndex());
        course6_grade = courseGrade6.getItemAt(courseGrade6.getSelectedIndex());

        double course1_grade_value = convertor(course1_grade);
        double course2_grade_value = convertor(course2_grade);
        double course3_grade_value = convertor(course3_grade);
        double course4_grade_value = convertor(course4_grade);
        double course5_grade_value = convertor(course5_grade);
        double course6_grade_value = convertor(course6_grade);
        
        credit1 = courseCredit1.getText();
        credit2 = courseCredit2.getText();
        credit3 = courseCredit3.getText();
        credit4 = courseCredit4.getText();
        credit5 = courseCredit5.getText();
        credit6 = courseCredit6.getText();


        if (credit1.isEmpty())
        {
            credit1_value = 0;
        }
        else
        {
            credit1_value = Integer.parseInt(credit1);
        }
        if (credit2.isEmpty())
        {
            credit2_value = 0;
        }
        else
        {
            credit2_value = Integer.parseInt(credit2);
        }
        if (credit3.isEmpty())
        {
            credit3_value = 0;
        }
        else
        {
            credit3_value = Integer.parseInt(credit3);
        }
        if (credit4.isEmpty())
        {
            credit4_value = 0;
        }
        else
        {
            credit4_value = Integer.parseInt(credit4);
        }
        if (credit5.isEmpty())
        {
            credit5_value = 0;
        }
        else
        {
            credit5_value = Integer.parseInt(credit5);
        }
        if (credit6.isEmpty())
        {
            credit6_value = 0;
        }
        else
        {
            credit6_value = Integer.parseInt(credit6);
        }
                
        int hourSum = credit1_value + credit2_value + credit3_value + credit4_value + credit5_value + credit6_value;
        double calc = ((credit1_value*course1_grade_value)+(credit2_value*course2_grade_value)+(credit3_value*course3_grade_value)
                +(credit4_value*course4_grade_value)+(credit5_value*course5_grade_value)+(credit6_value*course6_grade_value));
        gpa = calc / hourSum;
        String result = String.format("%.2f", gpa); // "3.14"
        gpaGrade.setText(result);
        
    }//GEN-LAST:event_calcButtonActionPerformed

